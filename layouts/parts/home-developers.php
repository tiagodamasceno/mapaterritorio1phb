        <p align="justify">A iniciativa tem amparo legal na Lei Complementar nº 061 de 21 de novembro
de 2014, que cria o Sistema Municipal de Informações e Indicadores
Culturais, SMIIC, instrumento de reconhecimento da *cidadania cultural *e
de gestão das políticas públicas no âmbito da cultura no município de
Parnaíba, sendo organizador e disponibilizador das informações cadastrais
sobre as diversas ações e bens culturais, bem como seus espaços e atores.</p>
        <p align="justify">Para saber mais sobre o projeto, acesse <a href="http://institutotim.org.br/project/mapas-culturais/" target="_blank">Mapas Culturais</a>.</p>
